package loopDemo;

public class Main {

	public static void main(String[] args) {
		
		//for loop...

		for ( int i = 0 ; i < 20 ; i++ ) {
									    System.out.println ( i ) ;
									    }
		          
		System.out.println(" End of forLoop... " );
		
		//while loop...
		
		int i=0;
		
		while ( i < 10 ) {
		
		   System.out.println ( i ) ;
		   i +=2 ;}
		   System.out.println ( "  End of whileLoop...  " ) ;
		
		//do while loop...
		   
		int j=15;
		
		do {
			   System.out.println ( j ) ;
			   j +=2 ;
			   
		}while(j < 30 );
		
		System.out.println ( "  End of dowhileloop...  " ) ;

	}

}
