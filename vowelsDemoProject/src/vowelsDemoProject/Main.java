package vowelsDemoProject;

public class Main {

	public static void main(String[] args) {
		char word = 'I' ;
		switch ( word ) {
		    case 'A' :
		    case 'I' :
		    case 'O' :
		    case 'U' :
		        System.out.println ( " vowel... " ) ;
		        break ;
		    default :
		        System.out.println ( " not vowel... " ) ;
		}
	}

}
